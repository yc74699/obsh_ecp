package com.xwtech.xwecp.service.logic.pojo;


public class CdrsPointBillDetail
{
	private String businessName;

	private String serviceCode;

	private String spendingTime;

	private String spendingPoint;
	
	private String spendingMoney;

	public String getBusinessName() {
		return businessName;
	}

	public void setBusinessName(String businessName) {
		this.businessName = businessName;
	}

	public String getServiceCode() {
		return serviceCode;
	}

	public void setServiceCode(String serviceCode) {
		this.serviceCode = serviceCode;
	}

	public String getSpendingTime() {
		return spendingTime;
	}

	public void setSpendingTime(String spendingTime) {
		this.spendingTime = spendingTime;
	}

	public String getSpendingPoint() {
		return spendingPoint;
	}

	public void setSpendingPoint(String spendingPoint) {
		this.spendingPoint = spendingPoint;
	}

	public String getSpendingMoney() {
		return spendingMoney;
	}

	public void setSpendingMoney(String spendingMoney) {
		this.spendingMoney = spendingMoney;
	}
	
	
}